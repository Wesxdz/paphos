PFN_vkVoidFunction getProc(const char* proc_name, VkInstance instance, VkDevice device)
{
    if (device != VK_NULL_HANDLE)
    {
        return vkGetDeviceProcAddr(device, proc_name);
    }
    return vkGetInstanceProcAddr(instance, proc_name);
}

